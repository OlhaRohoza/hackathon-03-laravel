        $owner = Owner::findOrFail($id);

        // dd($animal_image);
        $animals = DB::select(
            "SELECT *
            FROM owners
            LEFT JOIN animals ON owners.id = animals.owner_id
            WHERE owners.id = ?",
            [$id]
        );

        // dd($animals);
        return view('owners.detail', compact('owner', 'animals'));
    }

